                                    <h4>Hidrologi</h4>
                                    <p>Sesuasi dengan Topografi wilayah Desa Petak , terdapat sungai-sungai dari arah sungai Kromong semua bermuara di sungai desa Pacet dan Desa Sajen, sehingga menjadi tumpuhan banjir yang tidak mampu di tampung oleh masyarakat desa Petak tanpa ada bantuan dari pihak pemerintah.
                                        Wilayah Desa Petak banyak dialiri oleh anak sungai /saluran irigasi antara lain sungai Sungai Kromong sebelah Barat , dan Glibak sebelah Timur.
