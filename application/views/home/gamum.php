                                    <p>Kelembaban udara maksimum di Desa ini berkisar antara 88 – 100 persen dan kelembaban minimum antara 66 – 84 persen, sedangkan kelembaban rata-rata tiap bulan adalah 81 – 90 persen. Temperatur udara di suatu tempat antara lain ditentukan oleh tinggi rendahnya tempat tersebut terhadap permukaan laut dan jaraknya dari pantai. Temperatur maksimum di Desa Petak pada tahun 2008 berkisar antara 250C sampai 340C, temperatur minimum berkisar antara 180C sampai 280C dan rata-rata temperatur udara tiap bulan berkisar antara 210C sampai 300C.</p>
                                    <p>Curah hujan di suatu tempat antara lain dipengaruhi oleh keadaan iklim, keadaan geografi dan perputaran/pertemuan arus udara. Curah hujan tertinggi (maksimal) di Desa ini terjadi pada bulan Juni dan Nopember yaitu 105 mm sedangkan curah hujan terendah (minimal) terjadi pada bulan Agustus yaitu hanya 1 mm. Jumlah seluruh curah hujan selama tahun 2008 adalah 2.899,5 mm dan jumlah hari hujan adalah 144 hari.</p>
                                    <p>Antara curah hujan dan keadaan angin biasanya ada hubungan erat satu sama lain. Walaupun demikian di beberapa tempat, hubungan tersebut agaknya tidak selalu ada. Keadaan angin pada musim hujan biasanya lebih kencang dan angin bertiup dari barat dan barat laut. Oleh karena itu musim tersebut dikenal juga dengan musim barat. Pada musim kemarau angin bertiup dari benua Australia dan keadaan angin saat itu bisa juga kencang.</p>
                                    <p>Kecepatan angin di Desa Petak tiap bulannya berkisar antara 0,0 – 6,2 knot. Dan rata-rata penyinaran matahari yang dipantau pada pukul 06.00 – 18.00 terlihat intensitas yang hampir merata tiap bulannya (walaupun ada yang agak kurang intensitasnya). Penyinaran matahari dengan intensitas tertinggi terjadi pada bulan Juli yaitu 68 persen dan intensitas terendah terjadi pada bulan Desember yaitu 26 persen.</p>
