                                    <h4>Topografi</h4>
                                    <p>Desa Petak merupakan suatu bentuk permukaan bumi yang tidak teratur seluas 275,415 Ha, arah Barat berbatasan dengan Desa Kesimantengah arah timur berbatasan dengan Desa Bendunganjati / Desa Cepokolimo sebelah selatan berbatasan dengan Desa Pacet dan arah sebelah utara berbatasan dengan desa Warugunung. Adapun jarak Desa Petak ke kecamatan kurang lebih 0 km.</p>
                                    <p>Desa Petak dengan Ibukota Kecamatan nya dengan batas-batas, sebelah utara dan timur dengan Desa Pandanarum dan Desa Bendunganjati, sebelah selatan dengan Desa Pacet dan Desa Desa Cepokolimo, kemudian sebelah barat dengan Desa Kesimantengah. Dengan posisi geografis berada pada 11509’ – 1150 47’ Bujur Timur dan 10 18’ – 20 25’ Lintang Selatan.</p>
